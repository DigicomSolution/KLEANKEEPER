    // spider-teams filter function
    
	function filterList(value) {
		var list = $(".spider-team .spider-team-info");
		$(list).hide();
		if (value == "All") {
			$(".spider-team").find("article").each(function (i) {
				$(this).show();
			});
		} else {    
			// *=" means that if a data-custom type contains multiple values, it will find them
			$(".spider-team").find("article[data-custom-type*=" + value + "]").each(function (i) {
				$(this).show();
			});
		}
	}
